  <h2>Metodologia Geral</h2>
  <p>
    Os 11 livros serviram como fonte para o mapeamento dos nomes das/dos
    artistas a serem pesquisadas/pesquisados durante este projeto. Ou seja,
    as/os artistas citadas/citados nos livros foram as primeiras informações a
    serem consideradas. Entretanto, os dados sobre suas biografias foram
    buscados no Benezit Dictionary of Artists e no Grove Art Online (ambos fazem
    parte da plataforma Oxford Art Online). Caso não fosse encontrada alguma
    informação na primeira fonte, buscou-se em outra fonte digital confiável,
    sempre relatando na tabela qual foi o link da página utilizada. Com isso,
    temos controle da fonte da informação caso seja necessário confirmar a
    qualquer momento onde ela foi encontrada.
  </p>
  <p>
    A opção pela busca dos dados biográficos primeiramente no Benezit teve o
    intuito de padronizar a fonte e, por consequência, a própria metodologia de
    compilação das informações. Esse dicionário é reconhecio mundialmente nas
    universidades. Sua primeira edição impressa data de 1911 e o sistema on-line é
    atualizado três vezes por ano. </p> <p>  IMPORTANTE: Já na primeira reunião
    presencial com todos os integrantes do projeto, decidimos um critério que
    orientou nossa pesquisa: em caso de problemas e dúvidas, jamais escondermos
    nossas inquietações do público. Diferente da História da Arte normativa que
    queremos problematizar, não pretendemos utilizar recursos que mascarem
    procedimentos e posturas. É nosso interesse também mostrar a estrutura do
    projeto. Não consideramos que nossa pesquisa seja um discurso detentor de
    uma verdade absoluta, finalizada ou fechada. As tabelas que desenvolvemos
    nunca serão algo totalmente pronto. Nelas, há sempre a ideia de algo em
    construção contínua, como o próprio conhecimento humano.
  </p>

