  <h3>2. GÊNERO e RAÇA</h3>
  <p> Um exemplo de inquietação neste projeto foi a questão de gênero e raça.
    Como defini-los? Como encontrar termos precisos para definir a complexidade
    dos grupos excluídos? Como preencher essas duas categorias nas tabelas de
    dados? Inicialmente, Caroline Cotta de Mello Freitas (do conselho
    editorial) sugeriu o critério de autoidentificação para as artistas e os
    artistas, mas os livros não oferecem essa informação de maneira clara.
    Sempre consultando o conteúdo do dicionário Oxford Art Online, nesta etapa
    do projeto, preenchemos essas duas categorias somente para indicar
    negras/negros e mulheres. Seguindo a orientação de Vera Lúcia Benedito
    (também do conselho), essa estratégia provocativa de preenchimento das
    células somente nesses dois casos dá certa visibilidade a esses grupos
    praticamente invisíveis na História da Arte. </p>
  <p>É importante ressaltar que este projeto escolheu discutir a visibilidade
    das mulheres e da raça negra, apesar de estarmos cientes que há outras
    minorias que não estão retratadas nos livros analisados. Esta opção ocorreu
    por uma demanda de recorte do objeto e gostaríamos de lembrar que estamos
    abertos a continuidades das leituras propostas aqui. Pelo fato das outras
    células dessas duas categorias estarem em branco, existe a possibilidade de
    continuação em uma segunda etapa e indicar artistas transexuais, indígenas,
    aborígenes, por exemplo, citados nesses livros. Por causa dessas e outras
    ressalvas, as colunas de “gênero” e “raça” da tabela são assumidas no
    projeto como um ponto complexo e passível de problematizações e melhorias.
    A equipe do projeto está aberta a possíveis contestações e tem interesse de
    publicar no site as eventuais críticas sobre essa etapa. </p>
